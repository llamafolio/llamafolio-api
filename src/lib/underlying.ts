//retrieves details about underlying balances given an array of LP tokens

export async function getUnderlyingBalancesUniswap(chain: Chain, tokens: BaseBalance[]) {


  let calls = tokens.map((token) => {
    return {
      params: [],
      target: token.address,
    }
  })

  const token0Res = await multicall({
    chain: chain,
    calls: calls,
    abi: {
      "inputs": [],
      "name": "token0",
      "outputs": [
        {
          "internalType": "address",
          "name": "",
          "type": "address"
        }
      ],
      "stateMutability": "view",
      "type": "function"
    },
  });

  const token0Array = token0Res
    .filter((res) => res.success)
    .map((res) => res.output);

    const token1Res = await multicall({
      chain: chain,
      calls: calls,
      abi: {
        "inputs": [],
        "name": "token1",
        "outputs": [
          {
            "internalType": "address",
            "name": "",
            "type": "address"
          }
        ],
        "stateMutability": "view",
        "type": "function"
      },
    });

    const token1Array = token1Res
      .filter((res) => res.success)
      .map((res) => res.output);

    const totalSupplyRes = await multicall({
      chain: chain,
      calls: calls,
      abi: {
        "inputs": [],
        "name": "totalSupply",
        "outputs": [
          {
            "internalType": "uint256",
            "name": "",
            "type": "uint256"
          }
        ],
        "stateMutability": "view",
        "type": "function"
      },
    });

    const totalSupply = totalSupplyRes
      .filter((res) => res.success)
      .map((res) => BigNumber.from(res.output));


    calls = [];
    for (let index = 0; index < token0Array.length; index++) {
      const token = tokens[index];
      calls.push({
        params: [tokens[index].address],
        target: token0Array[index],
      });

      calls.push({
        params: [tokens[index].address],
        target: token1Array[index],
      });
    }

    const tokenDetails = await getERC20Details(chain, token1Array.concat(token0Array))


    const balancesOfRes = await multicall({
      chain: chain,
      calls: calls,
      abi: {
          "inputs": [
            {
              "internalType": "address",
              "name": "",
              "type": "address"
            }
          ],
          "name": "balanceOf",
          "outputs": [
            {
              "internalType": "uint256",
              "name": "",
              "type": "uint256"
            }
          ],
          "stateMutability": "view",
          "type": "function"
        },
    });

    const balancesOf = balancesOfRes
      .filter((res) => res.success)
      .map((res) => BigNumber.from(res.output));


    let balancesLoop = 0
    for (let index = 0; index < tokens.length; index++) {

      const token = tokens[index];

      const lpTotalSupply = BigNumber.from(totalSupply[index])
      let tokenBalances = {}
      const token0Detail = tokenDetails.find((o) => o.address === token0Array[index].toLowerCase());
      const token1Detail = tokenDetails.find((o) => o.address === token1Array[index].toLowerCase());

      tokenBalances["token0_userBalance"] = balancesOf[balancesLoop].mul(token.amount).div(lpTotalSupply)
      balancesLoop++
      tokenBalances["token1_userBalance"] = balancesOf[balancesLoop].mul(token.amount).div(lpTotalSupply)
      balancesLoop++

      tokens[index].details = [
        {
          address: token0Array[index],
          symbol: token0Detail.symbol,
          decimals: token0Detail.decimals,
          amount: tokenBalances["token0_userBalance"]
        },
        {
          address: token1Array[index],
          symbol: token1Detail.symbol,
          decimals: token1Detail.decimals,
          amount: tokenBalances["token1_userBalance"]
        }
      ]

    }

    return tokens
}


//retrieves underlying balances from a balance array where token0 and token1 have already been fetched
//mapped tokens must have been formatted by the getAllPairs method from the uniswap library

export async function getUnderlyingBalancesFromTokensUniswap(ctx, chain, mappedTokens) {


  let calls = mappedTokens.map((token) => {
    return {
      params: [],
      target: token.address
    }
  })

  let balanceCalls = mappedTokens.flatMap((bToken) => [{
    params: [bToken.address],
    target: bToken.token0.address
  }, {
    params: [bToken.address],
    target: bToken.token1.address
  }]);


  const totalSupplyRes = await multicall({
    chain: chain,
    calls: calls,
    abi: {
      "inputs": [],
      "name": "totalSupply",
      "outputs": [
        {
          "internalType": "uint256",
          "name": "",
          "type": "uint256"
        }
      ],
      "stateMutability": "view",
      "type": "function"
    },
  });

  const totalSupply = totalSupplyRes
    .filter((res) => res.success)
    .map((res) => BigNumber.from(res.output));

    const balancesOfRes = await multicall({
      chain: chain,
      calls: balanceCalls,
      abi: {
          "inputs": [
            {
              "internalType": "address",
              "name": "",
              "type": "address"
            }
          ],
          "name": "balanceOf",
          "outputs": [
            {
              "internalType": "uint256",
              "name": "",
              "type": "uint256"
            }
          ],
          "stateMutability": "view",
          "type": "function"
        },
    });

    const balancesOf = balancesOfRes
      .filter((res) => res.success)
      .map((res) => BigNumber.from(res.output));


  let balancesLoop = 0
  for (let index = 0; index < mappedTokens.length; index++) {
    const mappedRow = mappedTokens[index];

    const balance0 = balancesOf[balancesLoop].mul(mappedRow.amount).div(totalSupply[index])
    balancesLoop++
    const balance1 = balancesOf[balancesLoop].mul(mappedRow.amount).div(totalSupply[index])
    balancesLoop++

    mappedTokens[index].underlyings = [
      {
        chain,
        address: mappedRow.token0.address,
        symbol: mappedRow.token0.symbol,
        decimals: mappedRow.token0.decimals,
        amount: balance0
      },
      {
        chain,
        address: mappedRow.token1.address,
        symbol: mappedRow.token1.symbol,
        decimals: mappedRow.token1.decimals,
        amount: balance1
      }
    ]

  }

  return mappedTokens

}


